int main()
{
    // "Unelmia" is probably a placeholder name, but I like it
    auto window = sf::RenderWindow{ { 640u, 480u }, "Unelmia" };
    window.setFramerateLimit(144);

    Player* marie = new Player(100, 100);

    // create the tilemap from the level definition
    TileMap map;
    if (!map.load("assets/tilesets/test1.png", sf::Vector2u(16, 16), level, 40, 30))
        return -1;

    while (window.isOpen())
    {
        for (auto event = sf::Event{}; window.pollEvent(event);)
        {
            if (event.type == sf::Event::Closed)
            {
                window.close();
            }
        }

        window.clear();

        // main game loop
        marie->update(map);
        window.draw(map);
        window.draw(marie->sprite);
        printf("debug position print: %d", marie->getGlobalBounds());

        window.display();
    }
}
